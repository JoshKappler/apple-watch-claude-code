#!/usr/bin/env bash
# Pinch — one-shot named Cloudflare Tunnel setup.
#
# Turns a fresh `cloudflared tunnel login` into a fully-wired, stable wss:// host
# for the watch. Idempotent: re-running reuses an existing "pinch" tunnel and just
# rewrites the config/DNS, so it's safe to run again.
#
# PREREQUISITE (one-time, interactive — you must do this yourself, it opens a
# browser to authorize a zone on YOUR Cloudflare account):
#
#     cloudflared tunnel login
#
# Then run this with your chosen hostname (a subdomain of a domain that's on your
# Cloudflare account):
#
#     infra/cloudflared/setup-named-tunnel.sh pinch.yourdomain.com
#
# What it does:
#   1. Verifies you're logged in (~/.cloudflared/cert.pem).
#   2. Creates the named tunnel "pinch" (or reuses it) → gets its UUID + creds file.
#   3. Routes <hostname> → the tunnel (proxied CNAME at Cloudflare).
#   4. Writes ~/.cloudflared/config.yml mapping the host to ws://localhost:<PORT>.
#   5. Prints the exact wss:// URL to put in the watch (Settings → Server URL).
#
# After this, run the tunnel with:  infra/start-tunnel.sh
# Or install it always-on with:     infra/launchd/install-launchd.sh
set -euo pipefail

TUNNEL_NAME="${PINCH_TUNNEL_NAME:-pinch}"
CFG_DIR="$HOME/.cloudflared"
CONFIG_PATH="$CFG_DIR/config.yml"
REPO_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd)"
ENV_FILE="$REPO_ROOT/backend/.env"

bold(){ printf '\033[1m%s\033[0m\n' "$1"; }
ok(){   printf '  \033[32m✓\033[0m %s\n' "$1"; }
info(){ printf '  • %s\n' "$1"; }
warn(){ printf '  \033[33m!\033[0m %s\n' "$1"; }
die(){  printf '\033[31merror:\033[0m %s\n' "$1" >&2; exit 1; }

env_val(){ grep -E "^$1=" "$ENV_FILE" 2>/dev/null | head -n1 | cut -d= -f2- | tr -d '"' || true; }

HOSTNAME_ARG="${1:-}"
[[ -n "$HOSTNAME_ARG" ]] || die "usage: $0 <hostname>   e.g. $0 pinch.yourdomain.com"

command -v cloudflared >/dev/null 2>&1 || die "cloudflared not found — brew install cloudflared."

# 1. Logged in?
if [[ ! -f "$CFG_DIR/cert.pem" ]]; then
  die "not logged in. Run this FIRST (opens a browser):  cloudflared tunnel login"
fi
ok "cloudflared is logged in (cert.pem present)"

PORT="$(env_val PORT)"; PORT="${PORT:-8787}"

# 2. Create (or reuse) the named tunnel, then resolve its UUID.
existing_uuid="$(cloudflared tunnel list 2>/dev/null | awk -v n="$TUNNEL_NAME" '$2==n{print $1; exit}')"
if [[ -n "$existing_uuid" ]]; then
  UUID="$existing_uuid"
  ok "reusing existing tunnel '$TUNNEL_NAME' ($UUID)"
else
  bold "Creating tunnel '$TUNNEL_NAME'…"
  cloudflared tunnel create "$TUNNEL_NAME" >/dev/null
  UUID="$(cloudflared tunnel list 2>/dev/null | awk -v n="$TUNNEL_NAME" '$2==n{print $1; exit}')"
  [[ -n "$UUID" ]] || die "created the tunnel but could not read its UUID from 'cloudflared tunnel list'."
  ok "created tunnel '$TUNNEL_NAME' ($UUID)"
fi

CREDS="$CFG_DIR/$UUID.json"
[[ -f "$CREDS" ]] || die "credentials file not found at $CREDS (expected after tunnel create)."

# 3. Route the hostname to the tunnel (creates/repoints a proxied CNAME at Cloudflare).
bold "Routing $HOSTNAME_ARG → $TUNNEL_NAME…"
if cloudflared tunnel route dns "$TUNNEL_NAME" "$HOSTNAME_ARG" 2>/tmp/pinch-route.log; then
  ok "DNS route created"
else
  if grep -qiE "already exists|record with that host" /tmp/pinch-route.log; then
    warn "a DNS record for $HOSTNAME_ARG already exists — assuming it points at this tunnel"
    info "if the host is wrong, fix/delete the CNAME in the Cloudflare dashboard, then re-run"
  else
    cat /tmp/pinch-route.log >&2
    die "failed to route DNS. Is $HOSTNAME_ARG a subdomain of a domain on THIS Cloudflare account?"
  fi
fi

# 4. Write ~/.cloudflared/config.yml (idle-WS tuning matches config.example.yml).
mkdir -p "$CFG_DIR"
if [[ -f "$CONFIG_PATH" ]]; then
  cp "$CONFIG_PATH" "$CONFIG_PATH.bak.$(date +%s 2>/dev/null || echo prev)" 2>/dev/null || true
  warn "existing config.yml backed up alongside it"
fi
cat > "$CONFIG_PATH" <<YAML
# Pinch — named Cloudflare Tunnel ingress (generated by setup-named-tunnel.sh).
tunnel: $UUID
credentials-file: $CREDS

originRequest:
  connectTimeout: 30s
  tcpKeepAlive: 25s

ingress:
  - hostname: $HOSTNAME_ARG
    service: ws://localhost:$PORT
  - service: http_status:404
YAML
ok "wrote $CONFIG_PATH"

# 5. Tell the user the watch URL.
WSS="wss://$HOSTNAME_ARG"
echo
bar="────────────────────────────────────────────────────────"
bold "Done. Stable URL for the watch (Settings → Server URL):"
printf '\033[36m%s\033[0m\n' "$bar"
printf '  URL  \033[1m%s\033[0m\n' "$WSS"
printf '\033[36m%s\033[0m\n' "$bar"
info "Token is unchanged (PINCH_TOKEN in backend/.env)."
echo
bold "Next:"
info "Run it now:        infra/start-tunnel.sh"
info "Or always-on:      infra/launchd/install-launchd.sh"
info "This URL never changes again — no rebuilds to swap tunnels."
